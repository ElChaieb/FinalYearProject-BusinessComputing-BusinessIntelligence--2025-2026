def load_opportunities(df_op: pd.DataFrame) -> dict:
    """
    Inserts opportunities from the OP sheet into [opportunities].

    All rows are inserted with deleted = 1 (no quote yet).
    After load_quotes() commits, mark_won_opportunities() flips deleted = 0
    on every opportunity that has at least one linked quote.

    Source → target:
        OP.Oppo_OpportunityId  → opportunities.oppo_id    (PK)
        OP.Oppo_AssignedUserId → opportunities.user_id    (FK)
        OP.Chan_Description    → opportunities.agency_name
        OP.Oppo_CreatedDate    → opportunities.created_date
        OP.comp_reference      → opportunities.client_reference
        OP.Comp_Name           → clients.full_name  (via _get_or_create_client)
        OP.Emai_EmailAddress   → clients.email
        OP.Addr_City           → clients.city
    """
    report = {"total": 0, "inserted": 0, "skipped": 0, "errors": 0}

    df = (
        df_op
        .dropna(subset=["Oppo_OpportunityId"])
        .drop_duplicates(subset=["Oppo_OpportunityId"])
        .copy()
    )
    df["Oppo_CreatedDate"] = pd.to_datetime(
        df["Oppo_CreatedDate"], dayfirst=True, errors="coerce"
    )
    report["total"] = len(df)

    conn = get_connection()
    cur  = conn.cursor()
    try:
        for _, row in df.iterrows():
            oppo_id = _safe_int(row["Oppo_OpportunityId"])
            if oppo_id is None:
                report["skipped"] += 1
                continue

            cur.execute("SELECT 1 FROM opportunities WHERE oppo_id = ?", (oppo_id,))
            if cur.fetchone():
                report["skipped"] += 1
                continue

            user_id = _safe_int(row.get("Oppo_AssignedUserId"))

            client_id = _get_or_create_client(
                cur,
                full_name=row.get("Comp_Name"),
                email=row.get("Emai_EmailAddress"),
                city=row.get("Addr_City"),
            )

            agency_name      = _clean(row.get("Chan_Description"))
            client_reference = _clean(row.get("comp_reference"))
            raw_date         = row.get("Oppo_CreatedDate")
            created_date     = (
                raw_date.to_pydatetime()
                if pd.notna(raw_date) and hasattr(raw_date, "to_pydatetime")
                else None
            )

            cur.execute(
                """
                INSERT INTO opportunities
                    (oppo_id, user_id, client_id, agency_name,
                     created_date, client_reference, deleted)
                VALUES (?, ?, ?, ?, ?, ?, 1)
                """,
                (oppo_id, user_id, client_id, agency_name,
                 created_date, client_reference),
            )
            report["inserted"] += 1

        conn.commit()
        logger.info(
            f"Opportunities — inserted: {report['inserted']}, "
            f"skipped: {report['skipped']}, errors: {report['errors']}"
        )
    except Exception as exc:
        conn.rollback()
        logger.error(f"Error loading opportunities: {exc}")
        report["errors"] += 1
        raise
    finally:
        cur.close()
        conn.close()

    return report


def mark_won_opportunities() -> int:
    """
    Flips deleted = 0 on opportunities that now have at least one linked quote.
    Called after load_quotes() commits so the new quote rows are visible.
    """
    conn = get_connection()
    cur  = conn.cursor()
    try:
        cur.execute(
            """
            UPDATE opportunities
            SET    deleted = 0
            WHERE  deleted = 1
              AND  oppo_id IN (
                  SELECT DISTINCT oppo_id
                  FROM   quotes
                  WHERE  oppo_id IS NOT NULL
              )
            """
        )
        updated = cur.rowcount
        conn.commit()
        if updated:
            logger.info(f"Opportunities marked won (deleted=0): {updated}")
        return updated
    except Exception as exc:
        conn.rollback()
        logger.error(f"Error in mark_won_opportunities: {exc}")
        raise
    finally:
        cur.close()
        conn.close()


# ─────────────────────────────────────────────────────────────────────────────
# QUOTES  +  FAKE SALES
# ─────────────────────────────────────────────────────────────────────────────

def load_quotes(df_devis: pd.DataFrame) -> dict:
    """
    Inserts quotes from DEVIS into [quotes], then generates fake sales for
    10–23 % of the newly inserted quotes.

    All quotes start with deleted = 1.  After fake sales are inserted the
    "won" quotes are flipped to deleted = 0 in the same transaction.
    Then mark_won_opportunities() propagates the win upward.

    Source → target:
        DEVIS.Quot_OrderQuoteID  → quotes.quote_id   (PK)
        DEVIS.Quot_opportunityid → quotes.oppo_id    (FK, nullable)
        DEVIS.AR_Ref             → quotes.ar_ref     (FK)
        DEVIS.User_UserId        → quotes.user_id    (preferred)
        DEVIS.User_LastName/FirstName → fallback name lookup
        DEVIS.Quot_CreatedDate   → quotes.created_date
        opportunities.agency_name → quotes.agency_name  (inherited)
        opportunities.client_id  → quotes.client_id     (inherited)
        vehicles.base_price      → quotes.price          (looked up)
    """
    report = {
        "total": 0, "inserted": 0, "skipped": 0, "errors": 0,
        "sales_generated": 0,
    }

    if "Quot_OrderQuoteID" not in df_devis.columns:
        logger.warning("DEVIS missing 'Quot_OrderQuoteID' — skipping quotes.")
        return report

    df = (
        df_devis
        .dropna(subset=["Quot_OrderQuoteID"])
        .drop_duplicates(subset=["Quot_OrderQuoteID"], keep="first")
        .copy()
    )
    if "Quot_CreatedDate" in df.columns:
        df["Quot_CreatedDate"] = pd.to_datetime(
            df["Quot_CreatedDate"], dayfirst=True, errors="coerce"
        )
    else:
        df["Quot_CreatedDate"] = pd.NaT

    report["total"]  = len(df)
    has_user_id_col  = "User_UserId" in df.columns

    if not has_user_id_col:
        logger.warning(
            "DEVIS missing 'User_UserId' — resolving quote users by name."
        )

    user_cache:    dict = {}
    vehicle_cache: dict = {}
    oppo_cache:    dict = {}

    conn = get_connection()
    cur  = conn.cursor()
    try:
        newly_inserted: list[dict] = []

        for _, row in df.iterrows():
            quote_id = _safe_int(row["Quot_OrderQuoteID"])
            if quote_id is None:
                report["skipped"] += 1
                continue

            cur.execute("SELECT 1 FROM quotes WHERE quote_id = ?", (quote_id,))
            if cur.fetchone():
                report["skipped"] += 1
                continue

            # ── ar_ref + price ────────────────────────────────────────────
            ar_ref = _clean_raw(row.get("AR_Ref"))
            if ar_ref and ar_ref not in vehicle_cache:
                cur.execute(
                    "SELECT base_price FROM vehicles WHERE ar_ref = ?", (ar_ref,)
                )
                r = cur.fetchone()
                vehicle_cache[ar_ref] = r[0] if r else None
            price = vehicle_cache.get(ar_ref) if ar_ref else None

            # ── oppo_id FK (validate it exists) ──────────────────────────
            oppo_id = _safe_int(row.get("Quot_opportunityid"))
            if oppo_id is not None:
                if oppo_id not in oppo_cache:
                    cur.execute(
                        "SELECT 1 FROM opportunities WHERE oppo_id = ?", (oppo_id,)
                    )
                    oppo_cache[oppo_id] = cur.fetchone() is not None
                if not oppo_cache[oppo_id]:
                    logger.warning(
                        f"  quote {quote_id}: oppo_id {oppo_id} not found — "
                        "inserting with oppo_id = NULL"
                    )
                    oppo_id = None

            # ── user_id ───────────────────────────────────────────────────
            user_id = _resolve_quote_user(
                row, has_user_id_col, cur, user_cache, quote_id
            )

            # ── agency_name + client_id from linked opportunity ───────────
            agency_name = None
            client_id   = None
            if oppo_id is not None:
                cur.execute(
                    "SELECT agency_name, client_id FROM opportunities WHERE oppo_id = ?",
                    (oppo_id,),
                )
                r = cur.fetchone()
                if r:
                    agency_name, client_id = r[0], r[1]

            # ── created_date ──────────────────────────────────────────────
            raw_date     = row.get("Quot_CreatedDate")
            created_date = (
                raw_date.to_pydatetime()
                if pd.notna(raw_date) and hasattr(raw_date, "to_pydatetime")
                else None
            )

            # ── Insert with deleted = 1 ───────────────────────────────────
            cur.execute(
                """
                INSERT INTO quotes
                    (quote_id, oppo_id, ar_ref, user_id, client_id,
                     agency_name, price, created_date, deleted)
                VALUES (?, ?, ?, ?, ?, ?, ?, ?, 1)
                """,
                (quote_id, oppo_id, ar_ref, user_id, client_id,
                 agency_name, price, created_date),
            )
            report["inserted"] += 1
            newly_inserted.append({
                "quote_id":     quote_id,
                "oppo_id":      oppo_id,
                "ar_ref":       ar_ref,
                "user_id":      user_id,
                "client_id":    client_id,
                "agency_name":  agency_name,
                "price":        price,
                "created_date": created_date.date() if created_date else None,
            })

        # ── Fake sales ────────────────────────────────────────────────────
        won_quote_ids: list[int] = []

        if newly_inserted:
            rate   = random.uniform(FAKE_SALE_RATE_MIN, FAKE_SALE_RATE_MAX)
            k      = max(1, round(len(newly_inserted) * rate))
            chosen = random.sample(newly_inserted, min(k, len(newly_inserted)))
            today  = date.today()

            for q in chosen:
                sale_date = _random_date_after(q["created_date"], today)
                cur.execute(
                    """
                    INSERT INTO sales
                        (quote_id, oppo_id, user_id, client_id, ar_ref,
                         agency_name, sale_date, quantity, final_price)
                    SELECT ?, ?, ?, ?, ?, ?, ?, 1, ?
                    WHERE NOT EXISTS (
                        SELECT 1 FROM sales WHERE quote_id = ?
                    )
                    """,
                    (
                        q["quote_id"], q["oppo_id"], q["user_id"],
                        q["client_id"], q["ar_ref"],
                        q["agency_name"], sale_date, q["price"],
                        q["quote_id"],
                    ),
                )
                won_quote_ids.append(q["quote_id"])

            # Flip won quotes to deleted = 0
            if won_quote_ids:
                placeholders = ",".join("?" * len(won_quote_ids))
                cur.execute(
                    f"UPDATE quotes SET deleted = 0 WHERE quote_id IN ({placeholders})",
                    won_quote_ids,
                )
                logger.info(f"Quotes marked won (deleted=0): {len(won_quote_ids)}")

            report["sales_generated"] = len(chosen)
            pct = len(chosen) / len(newly_inserted) * 100
            logger.info(
                f"Fake sales generated: {len(chosen)} ({pct:.1f}% of new quotes)"
            )

        conn.commit()
        logger.info(
            f"Quotes — inserted: {report['inserted']}, "
            f"skipped: {report['skipped']}, errors: {report['errors']}"
        )

    except Exception as exc:
        conn.rollback()
        logger.error(f"Error loading quotes: {exc}")
        report["errors"] += 1
        raise
    finally:
        cur.close()
        conn.close()

    # Propagate wins to opportunities (needs its own connection after commit)
    if report["inserted"] > 0:
        mark_won_opportunities()

    return report


def _resolve_quote_user(row, has_user_id_col: bool, cur, cache: dict, quote_id: int):
    if has_user_id_col:
        uid       = _safe_int(row.get("User_UserId"))
        cache_key = ("id", uid)
        if cache_key not in cache:
            if uid is None:
                cache[cache_key] = None
            else:
                cur.execute("SELECT 1 FROM users WHERE user_id = ?", (uid,))
                cache[cache_key] = uid if cur.fetchone() else None
                if cache[cache_key] is None:
                    logger.warning(
                        f"  quote {quote_id}: user_id {uid} not found in users"
                    )
        return cache[cache_key]
    else:
        ln  = _clean(row.get("User_LastName"))
        fn  = _clean(row.get("User_FirstName"))
        cache_key = ("name", ln, fn)
        if cache_key not in cache:
            cache[cache_key] = _resolve_user_id_by_name(cur, ln, fn)
            if cache[cache_key] is None:
                logger.warning(
                    f"  quote {quote_id}: user '{fn} {ln}' not found in users"
                )
        return cache[cache_key]


def _random_date_after(start: date | None, end: date) -> date:
    if start is None or start >= end:
        return end
    delta = (end - start).days
    return start + timedelta(days=random.randint(1, delta))


