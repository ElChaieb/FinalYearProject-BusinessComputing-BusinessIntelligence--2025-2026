def _get_or_create_client(cur, full_name, email, city) -> int | None:
    """
    Look up or INSERT a client row using the caller's cursor (participates in
    the same transaction as the opportunity INSERT).

    Dedup key: (UPPER(full_name), LOWER(email))
    Falls back to name-only lookup when email is NULL.

    Returns client_id (generated by IDENTITY) or None if full_name is blank.
    """
    name_clean  = _clean_upper(full_name)
    email_clean = _clean_lower(email)
    city_clean  = _clean(city)

    if not name_clean:
        return None

    if email_clean:
        cur.execute(
            """
            SELECT TOP 1 client_id FROM clients
            WHERE UPPER(TRIM(full_name)) = ?
              AND LOWER(TRIM(email))     = ?
            """,
            (name_clean, email_clean),
        )
    else:
        cur.execute(
            """
            SELECT TOP 1 client_id FROM clients
            WHERE UPPER(TRIM(full_name)) = ?
              AND email IS NULL
            """,
            (name_clean,),
        )
    row = cur.fetchone()
    if row:
        return row[0]

    # INSERT — client_id is IDENTITY; retrieve it via OUTPUT INSERTED
    cur.execute(
        """
        INSERT INTO clients (full_name, email, city)
        OUTPUT INSERTED.client_id
        VALUES (?, ?, ?)
        """,
        (name_clean.title(), email_clean, city_clean),
    )
    new_row = cur.fetchone()
    client_id = new_row[0] if new_row else None
    logger.debug(f"  New client inserted: '{name_clean}' (id={client_id})")
    return client_id


