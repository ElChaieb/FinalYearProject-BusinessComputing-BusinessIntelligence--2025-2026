"""
load_to_opdb.py
───────────────────────────────────────────────────────────────────────────────
One-time migration script.

Reads every CRM xlsx file found in RAW_DIR (same OP + DEVIS structure as the
existing DWH ETL) and loads the data into the MS SQL Server OperationalDB
using the op_db_v3 schema.

Tables populated, in dependency order:
    users  →  vehicles  →  clients  →  opportunities  →  quotes  →  sales

Usage
-----
    # 1. Install dependencies (once)
    pip install pandas openpyxl python-dotenv pyodbc

    # 2. Create a .env file (or export env vars) with the SQL Server creds:
    #    OPDB_HOST, OPDB_PORT, OPDB_NAME, OPDB_USER, OPDB_PASSWORD, OPDB_DRIVER

    # 3. Drop your xlsx files in  ./etl/raw/  (or override RAW_DIR)

    # 4. Run
    python load_to_opdb.py

Configuration
-------------
All config is read from environment variables (with safe defaults).
See the CONFIG block below.

ID strategy
-----------
    user_id    taken directly from source (Oppo_AssignedUserId / User_UserId)
    oppo_id    taken directly from source (Oppo_OpportunityId)
    quote_id   taken directly from source (Quot_OrderQuoteID)
    ar_ref     taken directly from source (AR_Ref)
    client_id  IDENTITY — auto-generated by SQL Server; no source equivalent
    sale_id    IDENTITY — auto-generated by SQL Server

deleted semantics (derived, not sourced from CRM)
    opportunities.deleted = 1  →  no quote was ever created for it
    opportunities.deleted = 0  →  at least one quote exists  ("won")
    quotes.deleted        = 1  →  quote did not lead to a sale
    quotes.deleted        = 0  →  quote led to a sale

Fake sales
----------
The source files contain no sales data.  To populate the sales table with
realistic-looking data, a random 10–23 % of newly inserted quotes are chosen
as "won" and a sale row is generated for each.  The matching quote row is
then flipped to deleted = 0, and so is its parent opportunity.
"""

