_REQUIRED_OP = [
    "Oppo_OpportunityId", "Oppo_AssignedUserId",
    "Comp_Name", "Oppo_CreatedDate",
]
_REQUIRED_DEVIS = [
    "Quot_OrderQuoteID", "AR_Ref",
]


def _validate(df_op, df_devis, filename: str) -> dict:
    errors, warnings = [], []

    if df_op is None:
        errors.append("Sheet 'OP' is missing or empty")
    else:
        missing = [c for c in _REQUIRED_OP if c not in df_op.columns]
        if missing:
            errors.append(f"OP missing required columns: {missing}")

    if df_devis is None:
        errors.append("Sheet 'DEVIS' is missing or empty")
    else:
        missing = [c for c in _REQUIRED_DEVIS if c not in df_devis.columns]
        if missing:
            errors.append(f"DEVIS missing required columns: {missing}")
        if "User_UserId" not in (df_devis.columns if df_devis is not None else []):
            warnings.append("DEVIS missing 'User_UserId' — user fallback (name lookup) will be used for quotes")

    return {"valid": len(errors) == 0, "errors": errors, "warnings": warnings}


