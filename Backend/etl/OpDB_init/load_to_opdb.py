def load_vehicles(df_devis: pd.DataFrame) -> dict:
    """
    Extracts unique vehicles from DEVIS and inserts them into [vehicles].

    Source → target:
        DEVIS.AR_Ref    → vehicles.ar_ref      (PK)
        DEVIS.AR_Design → vehicles.ar_design
        DEVIS.Marque    → vehicles.brand
        DEVIS.Modèle    → vehicles.model
        catalogue       → vehicles.category
        catalogue       → vehicles.base_price
    """
    report = {"total": 0, "inserted": 0, "skipped": 0, "errors": 0}

    if "AR_Ref" not in df_devis.columns:
        logger.warning("DEVIS missing 'AR_Ref' — skipping vehicles.")
        return report

    keep = [c for c in ("AR_Ref", "AR_Design", "Marque", "Modèle") if c in df_devis.columns]
    vehicles_df = (
        df_devis[keep]
        .dropna(subset=["AR_Ref"])
        .drop_duplicates(subset=["AR_Ref"])
    )
    report["total"] = len(vehicles_df)

    conn = get_connection()
    cur  = conn.cursor()
    try:
        for _, row in vehicles_df.iterrows():
            ar_ref = _clean_raw(row["AR_Ref"])
            if not ar_ref:
                report["skipped"] += 1
                continue

            cur.execute("SELECT 1 FROM vehicles WHERE ar_ref = ?", (ar_ref,))
            if cur.fetchone():
                report["skipped"] += 1
                continue

            ar_design          = _clean(row.get("AR_Design"))
            brand              = _clean(row.get("Marque"))
            model              = _clean(row.get("Modèle"))
            category, base_price = _catalogue_lookup(ar_ref)

            cur.execute(
                """
                INSERT INTO vehicles (ar_ref, ar_design, brand, model, category, base_price)
                VALUES (?, ?, ?, ?, ?, ?)
                """,
                (ar_ref, ar_design, brand, model, category, base_price),
            )
            report["inserted"] += 1

        conn.commit()
        logger.info(
            f"Vehicles — inserted: {report['inserted']}, skipped: {report['skipped']}"
        )
    except Exception as exc:
        conn.rollback()
        logger.error(f"Error loading vehicles: {exc}")
        report["errors"] += 1
        raise
    finally:
        cur.close()
        conn.close()

    return report


# ─────────────────────────────────────────────────────────────────────────────
# CLIENTS  (called inline from load_opportunities via the caller's cursor)
