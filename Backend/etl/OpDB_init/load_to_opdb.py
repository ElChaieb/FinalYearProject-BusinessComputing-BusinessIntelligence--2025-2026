def load_users(df_op: pd.DataFrame, df_devis: pd.DataFrame) -> dict:
    """
    Extracts unique commercials from OP + DEVIS and inserts them into [users].
    Skips rows whose user_id already exists (no UPDATE — one-time load).

    Source → target:
        OP.Oppo_AssignedUserId  → users.user_id
        OP.User_LastName        → users.last_name
        OP.User_FirstName       → users.first_name
        OP.User_EmailAddress    → users.email
        OP.Chan_Description     → users.agency_name
        (hardcoded)             → users.role = 'Commercial'
    """
    report = {"total": 0, "inserted": 0, "skipped": 0, "errors": 0}
    users: dict[int, dict] = {}

    for _, row in df_op.iterrows():
        uid = _safe_int(row.get("Oppo_AssignedUserId"))
        if uid is None or uid in users:
            continue
        users[uid] = {
            "user_id":    uid,
            "last_name":  _clean(row.get("User_LastName")),
            "first_name": _clean(row.get("User_FirstName")),
            "email":      _clean_lower(row.get("User_EmailAddress")),
            "agency_name": _clean(row.get("Chan_Description")),
        }

    has_uid_col = "User_UserId" in df_devis.columns
    if not has_uid_col:
        logger.warning("DEVIS missing 'User_UserId' — no new users sourced from DEVIS.")
    else:
        for _, row in df_devis.iterrows():
            uid = _safe_int(row.get("User_UserId"))
            if uid is None or uid in users:
                continue
            users[uid] = {
                "user_id":    uid,
                "last_name":  _clean(row.get("User_LastName")),
                "first_name": _clean(row.get("User_FirstName")),
                "email":      None,   # DEVIS has no email column
                "agency_name": None,
            }

    report["total"] = len(users)
    if not users:
        logger.warning("No user data found — skipping users.")
        return report

    conn = get_connection()
    cur  = conn.cursor()
    try:
        for u in users.values():
            cur.execute("SELECT 1 FROM users WHERE user_id = ?", (u["user_id"],))
            if cur.fetchone():
                report["skipped"] += 1
                continue

            cur.execute(
                """
                INSERT INTO users (user_id, last_name, first_name, email, role, agency_name)
                VALUES (?, ?, ?, ?, ?, ?)
                """,
                (u["user_id"], u["last_name"], u["first_name"],
                 u["email"], "Commercial", u["agency_name"]),
            )
            report["inserted"] += 1

        conn.commit()
        logger.info(f"Users — inserted: {report['inserted']}, skipped: {report['skipped']}")
    except Exception as exc:
        conn.rollback()
        logger.error(f"Error loading users: {exc}")
        report["errors"] += 1
        raise
    finally:
        cur.close()
        conn.close()

    return report


def _resolve_user_id_by_name(cur, last_name: str, first_name: str) -> int | None:
    """Fallback user resolution when User_UserId is absent from DEVIS."""
    if not last_name or not first_name:
        return None
    cur.execute(
        """
        SELECT TOP 1 user_id FROM users
        WHERE UPPER(last_name)  = UPPER(?)
          AND UPPER(first_name) = UPPER(?)
        """,
        (last_name, first_name),
    )
    row = cur.fetchone()
    return row[0] if row else None


