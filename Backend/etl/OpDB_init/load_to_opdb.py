_DEFAULT_CATEGORY   = "Inconnu"
_DEFAULT_BASE_PRICE = None   # NULL — unknown variant; a wrong price is worse than NULL


def _catalogue_lookup(ar_ref: str) -> tuple[str, float | None]:
    entry = _CATALOGUE.get(ar_ref)
    if entry:
        return entry
    # Prefix match for unknown colour / trim variants of a known base model
    for key, val in _CATALOGUE.items():
        if ar_ref.startswith(key + "-") or key.startswith(ar_ref + "-"):
            return val
    return _DEFAULT_CATEGORY, _DEFAULT_BASE_PRICE


# =============================================================================
# SHARED HELPERS
# =============================================================================

def _safe_int(val) -> int | None:
    try:
        return int(val) if val is not None and not (
            isinstance(val, float) and pd.isna(val)
        ) else None
    except (ValueError, TypeError):
        return None


def _clean(val) -> str | None:
    if val is None or (isinstance(val, float) and pd.isna(val)):
        return None
    s = str(val).strip()
    return s.title() if s else None


def _clean_lower(val) -> str | None:
    if val is None or (isinstance(val, float) and pd.isna(val)):
        return None
    s = str(val).strip().lower()
    return s if s else None


def _clean_upper(val) -> str | None:
    if val is None or (isinstance(val, float) and pd.isna(val)):
        return None
    s = str(val).strip().upper()
    return s if s else None


def _clean_raw(val) -> str | None:
    """Strip only — no case change (used for ar_ref, agency codes, etc.)."""
    if val is None or (isinstance(val, float) and pd.isna(val)):
        return None
    s = str(val).strip()
    return s if s else None


